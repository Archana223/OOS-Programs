import queue
ar_priority_queue = queue.PriorityQueue()

element= [
    (2, "Hi! "),
    (1, "This is Python "),
    (3, "Program")
]

for i in element:
    ar_priority_queue.put(i)

print("Elements added to the priority queue:",)
for i in element:
    print(f"Priority: {i[0]}, Element: {i[1]}")


pop_element = ar_priority_queue.get()

print("\nElement popped from the priority queue:")
print(f"Priority: {pop_element[0]}, Element: {pop_element[1]}")
